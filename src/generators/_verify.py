"""
OenoBench — Independent-solver verification for low-quality generators.

Per the v2.1 plan §1, Llama and Qwen produce wrong-keyed questions ~30-40% of
the time. This module wires in a one-shot verification pass: every question
generated by Llama or Qwen is re-solved by an independent high-capability
model (Claude or Gemini, deterministically rotated) using the same source
fact(s). If the verifier disagrees with the keyed answer, the question is
rejected and the outer generator loop resamples.

Cost ledger (~April 2026 OpenRouter pricing per 1M tokens):
    claude   $3 in / $15 out
    gemini   $1.25 in / $10 out

Estimated impact: ~1,800 Llama/Qwen questions × ~1.5 retry factor × ~$0.005
per call ≈ $14 added cost for the full 10k generation run.
"""

from __future__ import annotations

import hashlib
from typing import Iterable

from loguru import logger

from src.generators._llm_client import GENERATOR_MODELS, LLMResponse, get_client

# ─── Configuration ────────────────────────────────────────────────────────────

# Generators whose questions must be independently verified.
GENERATORS_REQUIRING_VERIFICATION = {"llama", "qwen"}

# Verifier rotation pool — high-capability models that gold-review showed at
# 100% answer-correct on the audited sample.
VERIFIER_POOL: tuple[str, ...] = ("claude", "gemini")

# Pricing (per 1M tokens, USD). Mirrors src/qa/_judges._PRICING for the two
# verifier models so the cost ledger is consistent across audit + generation.
_PRICING: dict[str, tuple[float, float]] = {
    "claude": (3.0, 15.0),
    "gemini": (1.25, 10.0),
}


def _estimate_cost(model_short: str, input_tokens: int, output_tokens: int) -> float:
    """USD cost estimate for one verifier call. Same shape as _judges._estimate_cost."""
    in_cost, out_cost = _PRICING.get(model_short, (1.0, 5.0))
    return (input_tokens / 1_000_000) * in_cost + (output_tokens / 1_000_000) * out_cost


# ─── Verifier model selection ─────────────────────────────────────────────────


def _pick_verifier(question_text: str) -> str:
    """Stable hash-based rotation across the verifier pool.

    The same question text always picks the same verifier, so the pipeline is
    deterministic and reproducible from logs alone.
    """
    digest = hashlib.sha256(question_text.encode("utf-8")).digest()
    idx = int.from_bytes(digest[:4], "big") % len(VERIFIER_POOL)
    return VERIFIER_POOL[idx]


# ─── Prompt construction ──────────────────────────────────────────────────────

VERIFIER_SYSTEM = """\
You are an independent answer-key verifier for a wine-knowledge benchmark. \
You are given one or more SOURCE FACTS, a multiple-choice QUESTION, and the \
list of OPTIONS. Decide which option the source fact(s) most directly support, \
then return strict JSON. You have no opinions, no commentary, and no chain of \
thought — just the chosen letter and your confidence.

Rules:
1. Answer ONLY based on what the source facts explicitly state. Ignore any \
external wine knowledge you may have.
2. If multiple options seem plausible, pick the one most directly supported by \
the source. If none of the options is supported by the source, pick the one \
whose claim does not contradict the source.
3. Return your choice as a single uppercase letter (A, B, C, D, …).
4. Return your confidence as a float in [0.0, 1.0].
5. Output valid JSON only. No prose, no code fences."""


def _format_options_block(options: list[dict]) -> str:
    lines = []
    for opt in options:
        oid = str(opt.get("id", "")).strip()
        text = str(opt.get("text", "")).strip()
        lines.append(f"  {oid}) {text}")
    return "\n".join(lines)


def _format_facts_block(source_facts: Iterable[str]) -> str:
    facts = [f.strip() for f in source_facts if f and f.strip()]
    if not facts:
        return "(no source fact provided)"
    if len(facts) == 1:
        return facts[0]
    return "\n".join(f"[{i + 1}] {f}" for i, f in enumerate(facts))


def _build_verifier_prompt(
    question_text: str,
    options: list[dict],
    source_facts: list[str],
) -> str:
    facts_block = _format_facts_block(source_facts)
    options_block = _format_options_block(options)
    return (
        "SOURCE FACT(S):\n"
        f"{facts_block}\n\n"
        "QUESTION:\n"
        f"{question_text}\n\n"
        "OPTIONS:\n"
        f"{options_block}\n\n"
        "Return JSON: {\"chosen\": \"<A|B|C|D|...>\", \"confidence\": <0.0-1.0>}"
    )


# ─── Public API ───────────────────────────────────────────────────────────────


def verify_question_with_independent_solver(
    *,
    question_text: str,
    options: list[dict],
    correct_answer: str,
    source_facts: list[str],
    generator: str,
) -> tuple[bool, dict]:
    """Independently verify a question's answer key.

    Returns:
        (is_valid, debug_payload)

        is_valid is True when no verifier is needed (generator is not Llama/Qwen)
        OR when the verifier's chosen letter matches `correct_answer` (case- and
        whitespace-insensitive). is_valid is False when the verifier disagrees,
        when the verifier API call fails, or when the verifier emits an
        unparseable response — i.e. anything short of explicit agreement.

        debug_payload contains keys: verifier_model, chosen, confidence, raw,
        latency_ms, cost_usd, error (when applicable).
    """
    # Fast path: high-quality generators don't need verification.
    if generator not in GENERATORS_REQUIRING_VERIFICATION:
        return True, {
            "verifier_model": None,
            "skipped": True,
            "reason": f"generator '{generator}' not in verification set",
        }

    if not options:
        # Cannot verify open-ended questions — accept by default but log.
        logger.debug(
            "verify: skip (no options) | generator={} | question={!r}",
            generator, question_text[:80],
        )
        return True, {
            "verifier_model": None,
            "skipped": True,
            "reason": "no options to verify",
        }

    verifier_model = _pick_verifier(question_text)
    if verifier_model not in GENERATOR_MODELS:
        # Should never happen, but degrade gracefully.
        logger.error("verify: unknown verifier model '{}'", verifier_model)
        return True, {
            "verifier_model": verifier_model,
            "skipped": True,
            "reason": "verifier not registered",
        }

    prompt = _build_verifier_prompt(question_text, options, source_facts or [])
    client = get_client()
    response: LLMResponse = client.generate(
        prompt=prompt,
        system=VERIFIER_SYSTEM,
        model=verifier_model,
        temperature=0.0,
        max_tokens=200,
        json_mode=True,
    )

    cost_usd = _estimate_cost(verifier_model, response.input_tokens, response.output_tokens)

    if not response.success:
        logger.warning(
            "verify: LLM call failed | verifier={} | error={} | generator={}",
            verifier_model, response.error, generator,
        )
        return False, {
            "verifier_model": verifier_model,
            "chosen": None,
            "confidence": 0.0,
            "raw": response.error or "",
            "latency_ms": response.latency_ms,
            "cost_usd": cost_usd,
            "error": response.error,
        }

    parsed = response.parsed or {}
    raw_chosen = parsed.get("chosen", "")
    chosen = str(raw_chosen).strip().upper()[:1] if raw_chosen else None
    try:
        confidence = float(parsed.get("confidence", 0.0) or 0.0)
    except (TypeError, ValueError):
        confidence = 0.0

    debug = {
        "verifier_model": verifier_model,
        "chosen": chosen,
        "confidence": confidence,
        "raw": response.content[:500],
        "latency_ms": response.latency_ms,
        "cost_usd": cost_usd,
    }

    if chosen is None:
        logger.warning(
            "verify: unparseable verifier response | verifier={} | raw={!r}",
            verifier_model, response.content[:200],
        )
        return False, debug

    expected = (correct_answer or "").strip().upper()
    # Multi-select keys are comma-separated (e.g. "A,C"). For simplicity we
    # treat anything other than a single uppercase letter as agreement when
    # `chosen` is contained in the expected set (covers the rare multi-select
    # path; the dominant case is single-letter MC).
    expected_letters = {p.strip() for p in expected.split(",") if p.strip()}
    is_valid = chosen in expected_letters

    if is_valid:
        logger.info(
            "verify: AGREE | verifier={} | generator={} | chosen={} | conf={:.2f} | "
            "tokens={}+{} | cost=${:.4f}",
            verifier_model, generator, chosen, confidence,
            response.input_tokens, response.output_tokens, cost_usd,
        )
    else:
        logger.warning(
            "verify: DISAGREE | verifier={} | generator={} | chosen={} | "
            "expected={} | conf={:.2f} | cost=${:.4f}",
            verifier_model, generator, chosen, sorted(expected_letters),
            confidence, cost_usd,
        )

    return is_valid, debug


# ─── Template-specific verifier (v2.2 fix #8e) ──────────────────────────────
#
# For every TEMPLATE-generated question we route through Gemini (cheapest of
# the high-capability models) to pick the option most directly supported by
# the linked source fact. Gold-v2 showed 3 of 12 template questions were
# "completely incorrect" despite passing all existing gates — this verifier
# catches that class. At ~1000 templates × ~$0.001 = ~$1 for the run.

_TEMPLATE_VERIFIER_MODEL = "gemini"

_TEMPLATE_VERIFIER_SYSTEM = """\
You are verifying a multiple-choice wine question. Choose the option most \
DIRECTLY supported by the provided source fact. Do not use outside knowledge. \
If the fact does not directly support any option, answer N.

Output STRICT JSON with ONLY a single-letter `chosen` key: \
{"chosen": "A" | "B" | "C" | "D" | "N"}."""


def _build_template_verifier_prompt(
    question_text: str,
    options: list[dict],
    source_fact_text: str,
) -> str:
    options_block = _format_options_block(options)
    return (
        f"Source fact:\n{source_fact_text}\n\n"
        f"Question: {question_text}\n"
        f"Options:\n{options_block}\n\n"
        "Respond with JSON: {\"chosen\": \"A|B|C|D|N\"}."
    )


def verify_template_answer_with_gemini(
    *,
    question_text: str,
    options: list[dict],
    correct_answer_id: str,
    source_fact_text: str,
) -> tuple[bool, dict]:
    """v2.2 fix #8e — one Gemini call per template question, before insert.

    Returns (agrees, debug). `agrees` is True iff Gemini's chosen letter
    equals correct_answer_id (case-insensitive, first character). Any of
    {disagree, N=no-support, parse-failure, API-failure} → False, question
    rejected upstream.
    """
    if not options or not correct_answer_id:
        return True, {"skipped": True, "reason": "missing options or key"}

    prompt = _build_template_verifier_prompt(
        question_text=question_text,
        options=options,
        source_fact_text=source_fact_text or "",
    )
    client = get_client()
    response: LLMResponse = client.generate(
        prompt=prompt,
        system=_TEMPLATE_VERIFIER_SYSTEM,
        model=_TEMPLATE_VERIFIER_MODEL,
        temperature=0.0,
        max_tokens=40,
        json_mode=True,
    )

    cost_usd = _estimate_cost(
        _TEMPLATE_VERIFIER_MODEL, response.input_tokens, response.output_tokens
    )

    if not response.success:
        logger.warning(
            "template-verify: LLM call failed | error={} | question={!r}",
            response.error, question_text[:80],
        )
        return False, {
            "verifier_model": _TEMPLATE_VERIFIER_MODEL,
            "chosen": None,
            "error": response.error,
            "cost_usd": cost_usd,
        }

    parsed = response.parsed or {}
    chosen = str(parsed.get("chosen", "")).strip().upper()[:1] or None
    expected = (correct_answer_id or "").strip().upper()[:1]

    debug = {
        "verifier_model": _TEMPLATE_VERIFIER_MODEL,
        "chosen": chosen,
        "expected": expected,
        "raw": response.content[:300],
        "cost_usd": cost_usd,
    }

    if chosen is None:
        logger.warning("template-verify: unparseable response | raw={!r}", response.content[:200])
        return False, debug

    if chosen == "N":
        logger.warning(
            "template-verify: Gemini says fact does NOT support any option | q={!r}",
            question_text[:80],
        )
        return False, debug

    agrees = chosen == expected
    if agrees:
        logger.debug(
            "template-verify: AGREE | chosen={} | cost=${:.4f}", chosen, cost_usd
        )
    else:
        logger.warning(
            "template-verify: DISAGREE | chosen={} | expected={} | q={!r}",
            chosen, expected, question_text[:80],
        )
    return agrees, debug
